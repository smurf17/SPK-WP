    protected void updateDataTabel(){
        Object[] Baris = {
            "Kode Lowongan",
            "Divisi",
            "Kuota"            
        };
        
        tabmode = new DefaultTableModel(null, Baris);
        tabelLowongan.setModel(tabmode);

        String sql = "SELECT * FROM lowongan";
        
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String b = hasil.getString("kd_lowongan");
                String c = hasil.getString("divisi");
                String d = hasil.getString("kuota");                
                
                String[] data={ b, c, d};
                tabmode.addRow(data);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    

    private void tambahDataLowongan() {
        String sql = "INSERT INTO lowongan (kd_lowongan, divisi, kuota) VALUES (?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtKode.getText());
            stat.setString(2, txtDiv.getText());
            stat.setString(3, txtKuota.getText());
            stat.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "DATA Berhasil Disimpan");
            kosong();
        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan"+e);
        }
    }
    
    protected void kosong(){
        txtKode.setText("");
        txtDiv.setText("");
        txtKuota.setText("");        
    }

    private void editDataLowongan() {
        String sql = "UPDATE lowongan SET divisi=?, kuota=? WHERE kd_lowongan=?";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(3, txtKode.getText());
            stat.setString(1, txtDiv.getText());
            stat.setString(2, txtKuota.getText());
            stat.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "DATA Berhasil Diubah");
            kosong();
        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Diubah"+e);
        }
    }

    private void hapusDataLowongan() {
        int ok = JOptionPane.showConfirmDialog(null,"Hapus","Konfirmasi Dialog",JOptionPane.YES_NO_OPTION);
        if(ok == 0){
            String sql = "DELETE FROM lowongan WHERE kd_lowongan=?";
            try{
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, txtKode.getText());            
                stat.executeUpdate();

                JOptionPane.showMessageDialog(null, "DATA Berhasil Dihapus");
                kosong();
            } catch (SQLException e){
                JOptionPane.showMessageDialog(null, "Data Gagal Dihapus"+e);
            }
        }
    }
